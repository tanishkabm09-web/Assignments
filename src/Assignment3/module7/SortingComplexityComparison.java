package Assignment3.module7;

public class SortingComplexityComparison {



        public static void main(String[] args) {

            System.out.println("Sorting Algorithm Complexity Comparison\n");

            System.out.println("1. Bubble Sort");
            System.out.println("Best Time Complexity: O(n)");
            System.out.println("Average Time Complexity: O(n^2)");
            System.out.println("Worst Time Complexity: O(n^2)");
            System.out.println("Space Complexity: O(1)\n");

            System.out.println("2. Selection Sort");
            System.out.println("Best Time Complexity: O(n^2)");
            System.out.println("Average Time Complexity: O(n^2)");
            System.out.println("Worst Time Complexity: O(n^2)");
            System.out.println("Space Complexity: O(1)\n");

            System.out.println("3. Insertion Sort");
            System.out.println("Best Time Complexity: O(n)");
            System.out.println("Average Time Complexity: O(n^2)");
            System.out.println("Worst Time Complexity: O(n^2)");
            System.out.println("Space Complexity: O(1)\n");

            System.out.println("4. Merge Sort");
            System.out.println("Best Time Complexity: O(n log n)");
            System.out.println("Average Time Complexity: O(n log n)");
            System.out.println("Worst Time Complexity: O(n log n)");
            System.out.println("Space Complexity: O(n)\n");

            System.out.println("5. Quick Sort");
            System.out.println("Best Time Complexity: O(n log n)");
            System.out.println("Average Time Complexity: O(n log n)");
            System.out.println("Worst Time Complexity: O(n^2)");
            System.out.println("Space Complexity: O(log n)");
        }
    }

